    private void jb_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_eliminarActionPerformed
        try{
       Integer nro = Integer.parseInt(jtf_cod.getText());
       cdata.borrarComida(nro);
       } catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(this, "Debe ingresar un nÃºmero valido.");
      
       }
       limpiar();
    }//GEN-LAST:event_jb_eliminarActionPerformed

    private void jb_nuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_nuevoActionPerformed
        limpiar();
        c = null;
    }//GEN-LAST:event_jb_nuevoActionPerformed

    private void jb_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_guardarActionPerformed
        String nombre = jtf_nombre.getText();
        String tipo = jtf_tipo.getText();
        Integer calorias = Integer.parseInt(jtf_calorias.getText());
        String detalle = jtf_detalle.getText();
        Boolean baja = jrb_baja.isSelected();
        
        if(c==null){
            c = new Comida(nombre, tipo, calorias, detalle, baja);
            cdata.guardarComida(c);
        }
        
        limpiar();
    }//GEN-LAST:event_jb_guardarActionPerformed

    private void limpiar(){
        jtf_nombre.setText("");
        jtf_tipo.setText("");
        jtf_calorias.setText("");
        jtf_detalle.setText("");
        jrb_baja.setSelected(false);
        jtf_cod.setText("");
    }

